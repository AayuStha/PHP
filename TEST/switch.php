        <h1>About switch statement</h1>
    <?php
$fruit = "apple";

switch ($fruit) {
    case "apple":
        echo "Selected fruit is apple.";
        break;
    case "banana":
        echo "Selected fruit is banana.";
        break;
    case "orange":
        echo "Selected fruit is orange.";
        break;
    default:
        echo "Unknown fruit selected.";
}
?>
